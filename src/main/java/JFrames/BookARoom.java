    private void checkRoomsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkRoomsActionPerformed
        // TODO add your handling code here:
        //unselect all room toggle buttons
        for (JToggleButton i : rooms) {
            i.setSelected(false);
        }

        //clear roomSelected jList
        DefaultListModel roomsToClearModel = (DefaultListModel) roomSelected.getModel();
        roomsToClearModel.clear();
        
        //Receive input for check in and check out dates
        Date dateIn = checkInInput.getDate();
        Date dateOut = checkOutInput.getDate();
        ArrayList<String> datesNeeded = getDatesBetween(dateIn, dateOut);
        for (JToggleButton room : rooms) {
            room.setEnabled(true);
        }
        ArrayList<String> notAvailable = RoomAvailability.checkAvailability(datesNeeded, null);
        for (JToggleButton room : rooms) {
            if (notAvailable.contains(room.getText())) {
                room.setEnabled(false);
            }
        }

    }//GEN-LAST:event_checkRoomsActionPerformed

