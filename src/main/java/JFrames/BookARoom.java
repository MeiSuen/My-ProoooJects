    private void checkInInputPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_checkInInputPropertyChange
        // TODO add your handling code here:
        Date selected = checkInInput.getDate();
        if (selected != null) {
            // limit check out date dates after check In Date
            Calendar targetMinDate = Calendar.getInstance();
            targetMinDate.setTime(selected);
            targetMinDate.add(Calendar.DAY_OF_MONTH, 1);
            Date checkInOnwards = targetMinDate.getTime();
            checkOutInput.setMinSelectableDate(checkInOnwards);
            checkOutInput.setDate(null);
        }
    }//GEN-LAST:event_checkInInputPropertyChange


