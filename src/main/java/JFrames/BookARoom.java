    public void checkSelectedRoom(JToggleButton[] myRooms) {
        ActionListener toggleListener = (ActionEvent e) -> {
            JToggleButton[] rooms = myRooms;
            ArrayList<String> selectedRooms = new ArrayList<String>();
            for (JToggleButton i : rooms) {
                if (i.isSelected()) {
                    selectedRooms.add(i.getText());
                }
            }
            
            DefaultListModel<String> tempList = new DefaultListModel<>();
            for (String i : selectedRooms) {
                tempList.addElement(i);
            }
            roomSelected.setModel(tempList);
            updateCOnfirmBookingBtn();
        };
        for (JToggleButton i : myRooms) {
            i.addActionListener(toggleListener);
        }
    }

    public void updateCOnfirmBookingBtn() {
        DefaultListModel checkList = (DefaultListModel) roomSelected.getModel();
        boolean isEmpty = checkList.isEmpty();
        btnConfirmSelection.setEnabled(!isEmpty);
        ConfirmBtnWarning.setVisible(isEmpty);
    }

